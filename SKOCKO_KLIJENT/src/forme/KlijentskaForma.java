    private boolean proveriUnos() {
        
        boolean cifra1 = jTextFieldBroj1.getText().matches("[0-9]");
        boolean cifra2 = jTextFieldBroj2.getText().matches("[0-9]");
        boolean cifra3 = jTextFieldBroj3.getText().matches("[0-9]");
        boolean cifra4 = jTextFieldBroj4.getText().matches("[0-9]");
        
        if(cifra1 && cifra2 && cifra3 && cifra4){
            return true;
        }
        
        return false;
    }

    
