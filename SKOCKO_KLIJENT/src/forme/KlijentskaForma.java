    private void jButtonPosaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPosaljiActionPerformed
        
        if(!proveriUnos()){
            JOptionPane.showMessageDialog(null, "U svako polje mora biti upisana jedna cifra!", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(brojPokusaja<=5){
           String kombinacija = jTextFieldBroj1.getText()+"-"+jTextFieldBroj2.getText()+"-"+jTextFieldBroj3.getText()+"-"+jTextFieldBroj4.getText();
        System.out.println(kombinacija);
        
        Igra igra = new Igra(-1, kombinacija, -1, -1);
        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.POGADJAJ, igra);
        komunikacija.Komunikacija.getInstance().posaljiZahtev(kz);
        /////////////////
        
        ServerskiOdgovor so = komunikacija.Komunikacija.getInstance().primiOdgovor();
        igra = (Igra) so.getOdgovor();
        
        brojPokusaja++;
        
        jLabelNaMestu.setText(String.valueOf(igra.getBrojPogodjenihNaMestu()));
        jLabelVanMesta.setText(String.valueOf(igra.getBrojPogodjenihNisuNaMestu()));
        
        ModelTabeleIgra mti = (ModelTabeleIgra) jTableIgra.getModel();
        mti.dodajIgru(igra); 
            
            if(igra.getBrojPogodjenihNaMestu()==4){
                JOptionPane.showMessageDialog(null, "CESTITAMO, POBEDILI STE!");
                
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Nemate vise pokusaja!", "Izgubili ste", JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_jButtonPosaljiActionPerformed

